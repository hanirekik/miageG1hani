package com.example.car.rent;

public class Dates {
    private String begin;
    private String end;

    // Constructeur, getters et setters
    public Dates() {}

    public String getBegin() {
        return begin;
    }

    public void setBegin(String begin) {
        this.begin = begin;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }
}
